    public String[] Capitalize(String str[]){
        
        for(int i=0; i<2; i++){
            String first = str[i].substring(0,1).toUpperCase();    // first letter to upper
            String rem = str[i].substring(1,str[i].length()).toLowerCase();       // other letters to lower
            str[i]= first+rem;
        }
        String[] newName = {str[0], str[1]};
        return newName;
    }

