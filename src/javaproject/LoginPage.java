    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed

        String username = jTextFieldUsername.getText().trim();
        String password = new String(jPasswordFieldPassword.getPassword()).trim();

        if(username.isEmpty()){
            jLabelUsernameEmpty.setText("*required");
        }
        if (password.isEmpty()){
            jLabelPasswordEmpty.setText("*required");
        }
        if( !jRadioButtonAdmin.isSelected() && !jRadioButtonStudent.isSelected()){
            jLabelRadioButtonEmpty.setText("*required");
        }

        else{

            // TODO add your handling code here:
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/college","root","pi22/7");

                Statement statement = con.createStatement();
                String sql="";

                if(jRadioButtonAdmin.isSelected()){
                    sql = "select name from admins where username='"+username+"' and password ='"+password+"'";
                }
                else if(jRadioButtonStudent.isSelected()) {
                    sql = "select name from cs_student where username='"+username+"' and password ='"+password+"'";
                }

                ResultSet rs = statement.executeQuery(sql);

                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Login successful");
                    if(jRadioButtonAdmin.isSelected()){
                        String text = rs.getString("name");
                        
                        JavaProject page = new JavaProject();
                        page.openAdminHomePage(text);
                        this.dispose();
                    }

                    else{
                        String text = rs.getString("name");
                        
                        JavaProject page = new JavaProject();
                        page.openStudentHomePage(text);
                        
                        this.dispose();
                    }
                }
                else{
                    JOptionPane.showMessageDialog(this, "Incorrect login details");
                    jTextFieldUsername.setText("");
                    jPasswordFieldPassword.setText("");
                    buttonGroup1.clearSelection();
                }

            }
            catch (Exception e){
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

